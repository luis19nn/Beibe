package beibe.beans;

import java.io.Serializable;

/**
 *
 * @author luis
 */
public class Endereco implements Serializable {
    private int id;
    private Cidade cidade;
    private String rua;
    private int numero;
    private String complemento;
    private String bairro;
    private String cep;
    
    public Endereco(){}

    public Endereco(int id, Cidade cidade, String rua, int numero, String complemento, String bairro, String cep) {
        this.id = id;
        this.cidade = cidade;
        this.rua = rua;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.cep = cep;
    }
    
    public Endereco(Cidade cidade, String rua, int numero, String complemento, String bairro, String cep) {
        this.cidade = cidade;
        this.rua = rua;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.cep = cep;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Cidade getCidade() {
        return cidade;
    }

    public void setCidade(Cidade cidade) {
        this.cidade = cidade;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }
    
    
}
